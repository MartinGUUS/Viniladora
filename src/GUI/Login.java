    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = jTextField1.getText().toUpperCase().trim();
        String contrasena = jPasswordField1.getText().toUpperCase().trim();
        if (usuario.equalsIgnoreCase("") || contrasena.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Espacios vacios");
        } else {
            if (login(usuario, contrasena)) {
                MenuAdmin menuAdmin = new MenuAdmin();
                menuAdmin.setVisible(true);
                menuAdmin.setLocationRelativeTo(null);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
                jTextField1.setText("");
                jPasswordField1.setText("");
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

